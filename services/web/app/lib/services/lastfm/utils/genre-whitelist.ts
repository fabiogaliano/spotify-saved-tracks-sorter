/**
 * Check if a tag is a recognized genre
 */
export function isGenre(tag: string): boolean {
	return GENRE_WHITELIST.has(tag.toLowerCase())
}

/**
 * Filter a list of tags to only include recognized genres
 */
export function filterToGenres(tags: string[]): string[] {
	return tags.filter(tag => isGenre(tag))
}
